A = int(input("Veuillez Numero entre 1 et 7 :"))

if A == 1 :
    print("Lundi")
elif A == 2 :
    print("Mardi")
elif A == 3 :
    print("Mercredi")
elif A == 4 :
    print("Jeudi")
elif A == 5 :
    print("Vendredi")
elif A == 6 :
    print("Samedi")
elif A == 7 :
    print("Dimache")
else :
    print("ERROR")